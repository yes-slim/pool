int	ft_iterative_factorial(int nb)
{
	int	i;
	int	a;

	if (nb < 0)
		return (0);
	if (nb == 0 || nb == 1)
		return (1);
	i = 1;
	a = nb;
	while (i < a)
	{
		nb = nb * i;
		i++;
	}
	return (nb);
}
